// –°–¥–≤–∏–≥ –ø–æ —Ç–∞–±–ª–∏—Ü–µ Unicode —Ä–µ–∞–ª–∏–∑—É–µ—Ç—Å—è –ø—Ä–æ—Å—Ç—ã–º –ø—Ä–∏–±–∞–≤–ª–µ–Ω–∏–µ–º –∏–ª–∏ –≤—ã—á–∏—Ç–∞–Ω–∏–µ–º —Ä–∞–∑–º–µ—Ä–∞ —Å–¥–≤–∏–≥–∞ –∫ char —Å–∏–º–≤–æ–ª—É.

fun String.encrypt(base: Int): String {
    var res = ""
    for (i in this) {
        res += i + base
    }
    return res
}

fun String.decrypt(base: Int): String {
    var res = ""
    for (i in this) {
        res += i - base
    }
    return res
}

